a = None            # отсутствие данных
print(type(a))
a = 1               # целое число
print(type(a))
a = 1.0             # число с плавающей точкой
print(type(a))
a = 1 + 1j          # комплексное число
print(type(a))
a = '1'             # строка 
print(type(a))
a = [1, 1, 'a']     # список
print(type(a))
a = (1, 1, 'a')     # картеж
print(type(a))
a = {1, 2, 'a'}     # множество
print(type(a))
a = {'a':1, 'b':2}  # словарь
print(type(a))
a = True            # булевое значение
print(type(a))
